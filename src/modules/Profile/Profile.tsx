type User = {
  _id: string
  nickname: string
  name: string
  picture: string
  updated_at: string
  email: string
  email_verified: string
  sub: string
  sid: string
  age: number
  programCode: string
  referralCode: string
  points: number
  profile: {
    badges: Badge[]
    climateImpactReport: ClimateImpactReportProps
    summary: ProfileSummaryProps
    sharingCodes: Array<string>
    switchItPoints: PointsTotalProps
  }
}

const Profile = (): JSX.Element => {
  const dispatch = useDispatch()
  const user = useSelector((state: RootState) => state.user)
  const [userData, setUserData] = useState<User>(user)
  const {
    profile: {
      badges = [],
      sharingCodes = [],
      climateImpactReport = {},
      switchItPoints = [],
      summary = {},
    } = {},
  } = userData
  const [points, setTotalPoints] = useState(0)

  useEffect(() => {
    const totalPoints = switchItPoints.reduce((acc: number, { points }: any) => acc + points, 0)

    setTotalPoints(totalPoints)
  }, [switchItPoints])

  useEffect(() => {
    if (user) {
      setUserData(user)
    }
  }, [user, dispatch])

  return (
    <>
      <Head>
        <title>SwitchIt</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
        <meta name='viewport' content='width=device-width, initial-scale=1' />
      </Head>

      <S.Content>
        <S.ProfileContainer>
          <S.ProfileColumnUser>
            <Card column padded>
              <ProfileHead points={points} />
              <ProfileSummary data={summary} />
            </Card>
          </S.ProfileColumnUser>
          <S.ProfileColumn>
            <Card>
              <SharingCodes total={sharingCodes.length} />
            </Card>
            <Card>
              <Badges data={badges} />
            </Card>
            <Card column>
              <PointsTotal data={switchItPoints} points={points} />
            </Card>
          </S.ProfileColumn>
          <S.ProfileColumn>
            <Card>
              <ClimateImpactReport data={climateImpactReport} />
            </Card>
            <Card>
              <CheckList />
            </Card>
          </S.ProfileColumn>
        </S.ProfileContainer>
      </S.Content>
    </>
  )
}

export default Profile
